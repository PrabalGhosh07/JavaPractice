package Day2;

public class Loops {
	public static void main(String[] args) {
		//For loop[
        for (int i = 1; i <= 10; i++) {
            System.out.println("i = " + i);
        }
        
        //while loop
        int j = 11;
        while (j <= 15) {
            System.out.println("j = " + j);
            j++;
        }
        
        //Do while loop
        int k = 16;
        do {
            System.out.println("k = " + j);
            k++;
        } while (k <= 20);
    }

}
